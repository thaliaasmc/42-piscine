char	*ft_strlowcase(char *str)
{
	int	size;

	size = 0;
	while (str[size] != '\0')
	{
		if (str[size] >= 'A' && str[size] <= 'Z')
			str[size] += 32;
		size++;
	}
	return (str);
}
