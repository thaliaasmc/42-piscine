char	*ft_strupcase(char *str)
{
	int	size;

	size = 0;
	while (str[size] != '\0')
	{
		if (str[size] >= 'a' && str[size] <= 'z')
		{
			str[size] -= 32;
		}
		size++;
	}
	return (str);
}
